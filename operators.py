#Arithmetic Operator

num1 = 10
num2 = 20
print(num1+num2)
print(num1-num2)
print(num2/num1)
print(num1*num2)
print(num1**num2)
print(num2%num1)
print(num2//num1)

print(30%3)


print(12+4/2)               #BODMAS rule




